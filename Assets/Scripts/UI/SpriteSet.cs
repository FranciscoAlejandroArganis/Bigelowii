using UnityEngine;

/// <summary>
/// Conjunto de sprites usados en la interfaz de usuario
/// </summary>
public class SpriteSet : MonoBehaviour
{

    /// <summary>
    /// Botón vacío
    /// </summary>
    public Sprite empty;

    /// <summary>
    /// Botón de movimiento
    /// </summary>
    public Sprite move;

    /// <summary>
    /// Botón de terminar turno
    /// </summary>
    public Sprite endTurn;

    /// <summary>
    /// Botón de cancelar
    /// </summary>
    public Sprite cancel;

}
