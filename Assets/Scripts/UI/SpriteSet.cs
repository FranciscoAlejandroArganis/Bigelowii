using UnityEngine;

/// <summary>
/// Conjunto de sprites usados en la interfaz de usuario
/// </summary>
public class SpriteSet : MonoBehaviour
{

    /// <summary>
    /// Botón vacío
    /// </summary>
    public Sprite empty;

    /// <summary>
    /// Botón de movimiento
    /// </summary>
    public Sprite move;

    /// <summary>
    /// Botón de terminar turno
    /// </summary>
    public Sprite endTurn;

    /// <summary>
    /// Botón de confirmar
    /// </summary>
    public Sprite confirm;

    /// <summary>
    /// Botón de cancelar
    /// </summary>
    public Sprite cancel;

    /// <summary>
    /// Botón de avanzar al siguiente nivel
    /// </summary>
    public Sprite next;

    /// <summary>
    /// Botón de reintentar el último nivel
    /// </summary>
    public Sprite retry;

    /// <summary>
    /// Botón de atacar
    /// </summary>
    public Sprite attack;

    /// <summary>
    /// Ícono del evento de victoria
    /// </summary>
    public Sprite victory;

    /// <summary>
    /// Ícono de la esfera
    /// </summary>
    public Sprite sphere;

    /// <summary>
    /// Ícono del tetraedro
    /// </summary>
    public Sprite tetrahedron;

    /// <summary>
    /// Ícono del triángulo
    /// </summary>
    public Sprite triangle;

    /// <summary>
    /// Ícono del rectángulo
    /// </summary>
    public Sprite rectangle;

    /// <summary>
    /// Arreglo con los íconos de costos en conos
    /// </summary>
    public Sprite[] cones;

}
