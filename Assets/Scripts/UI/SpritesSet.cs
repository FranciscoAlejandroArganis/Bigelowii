using UnityEngine;

/// <summary>
/// Conjunto de sprites usados en la interfaz de usuario
/// </summary>
public class SpritesSet : ScriptableObject
{

    /// <summary>
    /// Botón vacío
    /// </summary>
    public Sprite empty;

    /// <summary>
    /// Botón de movimiento
    /// </summary>
    public Sprite move;

    /// <summary>
    /// Botón de terminar turno
    /// </summary>
    public Sprite endTurn;

    /// <summary>
    /// Botón de confirmar
    /// </summary>
    public Sprite confirm;

    /// <summary>
    /// Botón de cancelar
    /// </summary>
    public Sprite cancel;

    /// <summary>
    /// Botón de avanzar al siguiente nivel
    /// </summary>
    public Sprite next;

    /// <summary>
    /// Botón de reintentar el último nivel
    /// </summary>
    public Sprite retry;

    /// <summary>
    /// Botón de atacar
    /// </summary>
    public Sprite attack;

    /// <summary>
    /// Ícono de victoria
    /// </summary>
    public Sprite victory;

    /// <summary>
    /// Ícono de derrota
    /// </summary>
    public Sprite defeat;

    /// <summary>
    /// Ícono de la esfera
    /// </summary>
    public Sprite sphere;

    /// <summary>
    /// Ícono del tetraedro
    /// </summary>
    public Sprite tetrahedron;

    /// <summary>
    /// Ícono del octaedro
    /// </summary>
    public Sprite octahedron;

    /// <summary>
    /// Ícono del icosaedro
    /// </summary>
    public Sprite icosahedron;

    /// <summary>
    /// Ícono del triángulo
    /// </summary>
    public Sprite triangle;

    /// <summary>
    /// Ícono del rectángulo
    /// </summary>
    public Sprite rectangle;

    /// <summary>
    /// Ícono del rombo
    /// </summary>
    public Sprite rhombus;

    /// <summary>
    /// Ícono del círculo
    /// </summary>
    public Sprite circle;

    /// <summary>
    /// Ícono de la acción <c>Restoration</c>
    /// </summary>
    public Sprite restoration;

    /// <summary>
    /// Ícono de la acción <c>AntimatterBolt</c>
    /// </summary>
    public Sprite antimatterBolt;

    public Sprite callLightning;

    /// <summary>
    /// Arreglo con los íconos de costos en conos
    /// </summary>
    public Sprite[] cones;

    /// <summary>
    /// Arreglo con las vistas previas de los niveles
    /// </summary>
    public Sprite[] previews;

}
